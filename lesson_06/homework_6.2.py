#task- Напишіть цикл, який буде вимагати від користувача ввести слово, в якому є літера "h" (враховуються як великі так і маленькі).
# Цикл не повинен завершитися, якщо користувач ввів слово без букви "h".

while True:  # Цикл, який буде виконуватись нескінченно
    word = input("Введіть слово, в якому є літера 'h': ")  # Запитую у користувача слово
    if 'h' in word or 'H' in word:  # Перевіряя, чи є літера 'h' або 'H' в слові
        break  # Якщо умова виконується, зупиняю цикл